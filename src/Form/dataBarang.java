    private void btn_cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cariActionPerformed
    String keyword = t_cari.getText().trim();

    // Buat header kolom tabel
    Object[] Baris = {"Kode Barang", "Nama Barang", "Kategori", "Harga", "Stok"};
    tabmode = new DefaultTableModel(null, Baris);
    tblBarang.setModel(tabmode);

    // SQL cari berdasarkan kode atau nama
    String sql = "SELECT * FROM barang WHERE kode_barang LIKE ? OR nama_barang LIKE ?";

    try {
        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, "%" + keyword + "%");
        pst.setString(2, "%" + keyword + "%");

        ResultSet rs = pst.executeQuery();

        while (rs.next()) {
            String[] data = {
                rs.getString("kode_barang"),
                rs.getString("nama_barang"),
                rs.getString("kategori"),
                rs.getString("harga"),
                rs.getString("stok")
            };
            tabmode.addRow(data);
        }

        rs.close();
        pst.close();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Pencarian gagal: " + e.getMessage());
    }
    }//GEN-LAST:event_btn_cariActionPerformed

