    private void datatable() {
    Object[] kolom = {"Kode", "Nama", "Kategori", "Harga", "Stok Awal",
                      "Barang Masuk", "Barang Keluar", "Total Stok"};
    DefaultTableModel tabmode = new DefaultTableModel(null, kolom);
    DecimalFormat df = new DecimalFormat("#,##0.00");

    try {
        String sql = "SELECT kode_barang, nama_barang, kategori, harga, " +
                     "stok AS stok_awal, barang_masuk, barang_keluar, total_stok " +
                     "FROM barang ORDER BY kode_barang";
        PreparedStatement pst = conn.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();

        while (rs.next()) {
            Object[] data = {
                rs.getString("kode_barang"),
                rs.getString("nama_barang"),
                rs.getString("kategori"),
                df.format(rs.getDouble("harga")),
                rs.getInt("stok_awal"),
                rs.getInt("barang_masuk"),
                rs.getInt("barang_keluar"),
                rs.getInt("total_stok")
            };
            tabmode.addRow(data);
        }

        tblBarang.setModel(tabmode);

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Gagal menampilkan data: " + e.getMessage());
    }
    }
